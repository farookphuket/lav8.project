     */
    public function store()
    {
        //
        $al_id = 0;
        if(Auth::user()->id):
        
            $al = request()->album;

            $get = Album::where("name" ,$al)->get();
            if(count($get) == 0):
                // only if cannot find then create one
                $al_id = Album::create([
                    "name" => $al
                ]);
            endif;
            foreach($get as $row):
                $al_id = $row->id;
            endforeach;
        endif;
       return response()->json(["album" => $al_id]); 
    }

